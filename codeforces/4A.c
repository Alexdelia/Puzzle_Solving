#include "stdio.h"
#include "unistd.h"

int	main(void)
{
	unsigned short	n;

	scanf("%hu", &n);
	if (n % 2 == 0 && n > 2)
		write(1, "YES", 3);
	else
		write(1, "NO", 2);
	return (0);
}
