#include <stdio.h>

int	main(void)
{
	int	n;
	int sum;

	n = 1;
	sum = 0;
	while (n < 1000)
	{
		if (n % 3 == 0 || n % 5 == 0)
			sum += n;
		n++;
	}
	printf("sum: %d\n", sum);
	return (0);
}
